package model;

/**
 *
 * @author ACER
 */
public class BanAn {
    private int maBan;
    private String trangThai;

    public BanAn() {}

    public BanAn(int maBan, String trangThai) {
        this.maBan = maBan;
        this.trangThai = trangThai;
    }

    public int getMaBan() {
        return maBan;
    }

    public void setMaBan(int maBan) {
        this.maBan = maBan;
    }

    public String getTrangThai() {
        return trangThai;
    }

    public void setTrangThai(String trangThai) {
        this.trangThai = trangThai;
    }
}
